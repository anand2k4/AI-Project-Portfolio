           #  List
# Example
reviews= ["Good movie," "Bad acting","Amazing story"]
print(reviews)

# Access items:
print(reviews[0])  
print(reviews[1])

# Add item: 
reviews.append("Excellent direction")

# Lenght:
print(len(reviews))

# Loop : for loop: (going through each item one by one)
# while loops repeatedly excecute a block of code as long as a given condition remains true.
for review in reviews:
    print(review)


